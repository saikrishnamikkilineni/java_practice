public class Product {

  int pId,pqty;
  String pName;
  double price;

  public void setpId(int pId) {
    this.pId = pId;
  }

  public int getpId() {
    return pId;
  }

  public void setpName(String pName) {
    this.pName = pName;
  }

  public String getpName() {
    return pName;
  }

  public void setprice(double price) {
    this.price = price;
  }

  public double getprice() {
    return price;
  }

  public void setpqty(int pqty) {
    this.pqty=pqty;
  }

  public int getpqty() {
    return pqty;
  }

  
}